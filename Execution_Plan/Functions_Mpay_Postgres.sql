---GET_SUM_OF_HOLD_AMOUNT

CREATE OR REPLACE FUNCTION GET_SUM_OF_HOLD_AMOUNT(receiverIdentifier VARCHAR)
RETURNS NUMERIC AS $$
DECLARE
    sumOfHoldAmount NUMERIC;
BEGIN
    SELECT COALESCE(SUM(hr.HOLDAMOUNT), 0) INTO sumOfHoldAmount
    FROM MEPS_HOLDRECORDS hr
    WHERE hr.TRANSACTIONREFERENCEID IN (
        SELECT mt.ID
        FROM MEPS_TRANSACTIONS mt
        WHERE mt.HASHOLDAMOUNT = 1
        AND mt.EXTRAFIELD1 = receiverIdentifier
    );
    
    RETURN sumOfHoldAmount;
END;
$$ LANGUAGE plpgsql;


---INCREASE_TOTAL_HOLD_AMOUNTS

CREATE OR REPLACE FUNCTION INCREASE_TOTAL_HOLD_AMOUNTS(
    isMobile NUMERIC,
    transactionRef VARCHAR,
    holdAmount NUMERIC
)
RETURNS INTEGER AS $$
DECLARE
    rows_updated INTEGER;
BEGIN
    IF isMobile > 0 THEN
        UPDATE mpay_accounts
        SET TOTALHOLDAMOUNTS = TOTALHOLDAMOUNTS + holdAmount
        WHERE id = (
            SELECT ma.refaccountid
            FROM MPAY_MOBILEACCOUNTS ma
            WHERE ma.id = (
                SELECT mt.RECEIVERMOBILEACCOUNTID
                FROM mpay_transactions mt
                WHERE mt.REFERENCE = transactionRef
            )
        );
    ELSE
        UPDATE mpay_accounts
        SET TOTALHOLDAMOUNTS = TOTALHOLDAMOUNTS + holdAmount
        WHERE id = (
            SELECT sa.refaccountid
            FROM MPAY_SERVICEACCOUNTS sa
            WHERE sa.id = (
                SELECT mt.RECEIVERSERVICEACCOUNTID
                FROM mpay_transactions mt
                WHERE mt.REFERENCE = transactionRef
            )
        );
    END IF;
    
    GET DIAGNOSTICS rows_updated = ROW_COUNT;
    RETURN rows_updated;
END;
$$ LANGUAGE plpgsql;

