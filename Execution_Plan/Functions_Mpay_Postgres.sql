CREATE OR REPLACE FUNCTION postjournalvoucher(in accountid int8, in amount numeric, in jvid int8, out vrowscount int4, out vbalanceafter numeric, out vbalancebefore numeric)
  RETURNS record AS 
$BODY$
DECLARE
VTYPE int8;
    voucherId bigint;
	UPD_ROW_COUNT int;

BEGIN
SELECT BalanceTypeID, Balance
INTO vType, vBalanceBefore
FROM MPAY_Accounts
WHERE ID = accountId for update;
vBalanceAfter := vBalanceBefore + amount;

IF vType = 1 THEN
UPDATE MPAY_Accounts SET balance = vBalanceAfter
WHERE id = accountId AND vBalanceAfter >= 0;
ELSE IF vType = 2 THEN
UPDATE MPAY_Accounts SET balance = vBalanceAfter
WHERE id = accountId AND vBalanceAfter <= 0;
ELSE
UPDATE MPAY_Accounts SET balance = vBalanceAfter
WHERE id = accountId;
END IF;
END IF;
GET DIAGNOSTICS UPD_ROW_COUNT = ROW_COUNT;
vRowsCount := UPD_ROW_COUNT;
END;
$BODY$
  LANGUAGE 'plpgsql' COST 100.0 SECURITY INVOKER;

