--
-- calculate_limits
-- 

CREATE OR REPLACE FUNCTION calculate_limits(in mobileid int8, in typeid int8, out dailycount float8, out dailyamount numeric, out weeklycount float8, out weeklyamount numeric, out monthlycount float8, out monthlyamount numeric, out yearlycount float8, out yearlyamount numeric)
  RETURNS record AS 
$BODY$
   DECLARE
   MOBILENUMBER  MPAY_CUSTOMERMOBILES.MOBILENUMBER%TYPE;
BEGIN
   SELECT m.MOBILENUMBER INTO STRICT MOBILENUMBER FROM MPAY_CUSTOMERMOBILES m WHERE ID = MOBILEID;

   BEGIN
      SELECT SUM(TRANSACTIONS.TOTALAMOUNT) INTO STRICT DAILYAMOUNT FROM MPAY_TRANSACTIONS TRANSACTIONS, MPAY_ENDPOINTOPERATIONS OPERATIONS, MPAY_MPAYMESSAGES MESSAGES
      WHERE TRANSACTIONS.REFMESSAGEID = MESSAGES.ID
      AND MESSAGES.REFOPERATIONID = OPERATIONS.ID
      AND OPERATIONS.MESSAGETYPEID = TYPEID
      AND TRANSACTIONS.SENDERMOBILEID = MOBILEID
      AND TRUNC(TRANSACTIONS.TRANSDATE) = DATE_TRUNC('DAY',LOCALTIMESTAMP)
      AND TRANSACTIONS.PROCESSINGSTATUSID IN(1,2,4);
      IF DAILYAMOUNT IS NULL THEN
         DAILYAMOUNT := 0;
      END IF;
   END;
   SELECT COUNT(MESSAGES.ID) INTO STRICT DAILYCOUNT FROM MPAY_MPAYMESSAGES MESSAGES, MPAY_ENDPOINTOPERATIONS OPERATIONS
   WHERE MESSAGES.REFOPERATIONID = OPERATIONS.ID
   AND OPERATIONS.MESSAGETYPEID = TYPEID
   AND MESSAGES.SENDER = MOBILENUMBER
   AND TRUNC(MESSAGES.PROCESSINGSTAMP) = DATE_TRUNC('DAY',LOCALTIMESTAMP)
   AND MESSAGES.PROCESSINGSTATUSID IN(1,2,4);
   BEGIN
      SELECT SUM(TRANSACTIONS.TOTALAMOUNT) INTO STRICT WEEKLYAMOUNT FROM MPAY_TRANSACTIONS TRANSACTIONS, MPAY_ENDPOINTOPERATIONS OPERATIONS, MPAY_MPAYMESSAGES MESSAGES
      WHERE TRANSACTIONS.REFMESSAGEID = MESSAGES.ID
      AND MESSAGES.REFOPERATIONID = OPERATIONS.ID
      AND OPERATIONS.MESSAGETYPEID = TYPEID
      AND TRANSACTIONS.SENDERMOBILEID = MOBILEID
      AND EXTRACT(YEAR FROM TRANSACTIONS.TRANSDATE) =  EXTRACT(YEAR FROM LOCALTIMESTAMP)
      AND CAST(TO_CHAR(TRANSACTIONS.TRANSDATE,'WW') AS DOUBLE PRECISION) =  CAST(TO_CHAR(LOCALTIMESTAMP,'WW') AS BIGINT)
      AND TRANSACTIONS.PROCESSINGSTATUSID IN(1,2,4);
      IF WEEKLYAMOUNT IS NULL THEN
         WEEKLYAMOUNT := 0;
      END IF;
   END;

   SELECT COUNT(MESSAGES.ID) INTO STRICT WEEKLYCOUNT FROM MPAY_MPAYMESSAGES MESSAGES, MPAY_ENDPOINTOPERATIONS OPERATIONS
   WHERE MESSAGES.REFOPERATIONID = OPERATIONS.ID
   AND OPERATIONS.MESSAGETYPEID = TYPEID
   AND MESSAGES.SENDER = MOBILENUMBER
   AND EXTRACT(YEAR FROM MESSAGES.PROCESSINGSTAMP) =  EXTRACT(YEAR FROM LOCALTIMESTAMP)
   AND CAST(TO_CHAR(MESSAGES.PROCESSINGSTAMP,'WW') AS DOUBLE PRECISION) =  CAST(TO_CHAR(LOCALTIMESTAMP,'WW') AS BIGINT)
   AND MESSAGES.PROCESSINGSTATUSID IN(1,2,4);

   BEGIN
      SELECT SUM(TRANSACTIONS.TOTALAMOUNT) INTO STRICT MONTHLYAMOUNT FROM MPAY_TRANSACTIONS TRANSACTIONS, MPAY_ENDPOINTOPERATIONS OPERATIONS, MPAY_MPAYMESSAGES MESSAGES
      WHERE TRANSACTIONS.REFMESSAGEID = MESSAGES.ID
      AND MESSAGES.REFOPERATIONID = OPERATIONS.ID
      AND OPERATIONS.MESSAGETYPEID = TYPEID
      AND TRANSACTIONS.SENDERMOBILEID = MOBILEID
      AND EXTRACT(YEAR FROM TRANSACTIONS.TRANSDATE) =  EXTRACT(YEAR FROM LOCALTIMESTAMP)
      AND EXTRACT(MONTH FROM TRANSACTIONS.TRANSDATE) =  EXTRACT(MONTH FROM LOCALTIMESTAMP)
      AND TRANSACTIONS.PROCESSINGSTATUSID IN(1,2,4);
      IF MONTHLYAMOUNT IS NULL THEN
         MONTHLYAMOUNT := 0;
      END IF;
   END;

   SELECT COUNT(MESSAGES.ID) INTO STRICT MONTHLYCOUNT FROM MPAY_MPAYMESSAGES MESSAGES, MPAY_ENDPOINTOPERATIONS OPERATIONS
   WHERE MESSAGES.REFOPERATIONID = OPERATIONS.ID
   AND OPERATIONS.MESSAGETYPEID = TYPEID
   AND MESSAGES.SENDER = MOBILENUMBER
   AND EXTRACT(YEAR FROM MESSAGES.PROCESSINGSTAMP) =  EXTRACT(YEAR FROM LOCALTIMESTAMP)
   AND EXTRACT(MONTH FROM MESSAGES.PROCESSINGSTAMP) =  EXTRACT(MONTH FROM LOCALTIMESTAMP)
   AND MESSAGES.PROCESSINGSTATUSID IN(1,2,4);


   BEGIN
      SELECT SUM(TRANSACTIONS.TOTALAMOUNT) INTO STRICT YEARLYAMOUNT FROM MPAY_TRANSACTIONS TRANSACTIONS, MPAY_ENDPOINTOPERATIONS OPERATIONS, MPAY_MPAYMESSAGES MESSAGES
      WHERE TRANSACTIONS.REFMESSAGEID = MESSAGES.ID
      AND MESSAGES.REFOPERATIONID = OPERATIONS.ID
      AND OPERATIONS.MESSAGETYPEID = TYPEID
      AND TRANSACTIONS.SENDERMOBILEID = MOBILEID
      AND EXTRACT(YEAR FROM TRANSACTIONS.TRANSDATE) =  EXTRACT(YEAR FROM LOCALTIMESTAMP)
      AND TRANSACTIONS.PROCESSINGSTATUSID IN(1,2,4);
      IF YEARLYAMOUNT IS NULL THEN
         YEARLYAMOUNT := 0;
      END IF;
   END;


   SELECT COUNT(MESSAGES.ID) INTO STRICT YEARLYCOUNT FROM MPAY_MPAYMESSAGES MESSAGES, MPAY_ENDPOINTOPERATIONS OPERATIONS
   WHERE MESSAGES.REFOPERATIONID = OPERATIONS.ID
   AND OPERATIONS.MESSAGETYPEID = TYPEID
   AND MESSAGES.SENDER = MOBILENUMBER
   AND EXTRACT(YEAR FROM MESSAGES.PROCESSINGSTAMP) =  EXTRACT(YEAR FROM LOCALTIMESTAMP)
   AND MESSAGES.PROCESSINGSTATUSID IN(1,2,4);
   RETURN;
END; $BODY$
  LANGUAGE 'plpgsql' COST 100.0 SECURITY INVOKER;

