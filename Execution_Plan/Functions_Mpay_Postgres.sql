  RETURNS numeric AS 
$BODY$
declare
            clients_total_balance numeric ;
            BEGIN
            SELECT sum(BALANCE)
            INTO clients_total_balance
            FROM MPAY_ACCOUNTS
            WHERE ID in (select refaccountid
                         from mpay_serviceaccounts ms
                         where serviceid in (select id
                                             from mpay_corpoarteservices mc
                                             where servicewallettypeid in
                                                   (select id from mpay_wallettypes mw where code = wallet_type_code)))
               OR ID IN (select refaccountid
                         from MPAY_MOBILEACCOUNTS ms
                         where mobileId in (select id
                                            from MPAY_CUSTOMERMOBILES mc
                                            where mobilewallettypeid in
                                                  (select id from mpay_wallettypes mw where code = wallet_type_code)));
            return clients_total_balance;
            END;
$BODY$
  LANGUAGE 'plpgsql' COST 100.0 SECURITY INVOKER;

