--
-- update_clients_limits
-- 

CREATE OR REPLACE FUNCTION update_clients_limits(in paccountid int8, in pmessagetypeid int8, in pamount numeric, in pcount int8, in pdate date, out rrowcount int4)
  RETURNS int4 AS 
$BODY$
   DECLARE
   RID  MPAY_CLIENTSLIMITS.ID%TYPE;
   VCOUNT  DOUBLE PRECISION;
   VYEAR  MPAY_CLIENTSLIMITS.YEAR%TYPE;
   VDAILYDATE  MPAY_CLIENTSLIMITS.DAILYDATE%TYPE;
   VWEEKNUMBER  MPAY_CLIENTSLIMITS.WEEKNUMBER%TYPE;
   VMONTH  MPAY_CLIENTSLIMITS.MONTH%TYPE;
   VDAILYCOUNT  MPAY_CLIENTSLIMITS.DAILYCOUNT%TYPE;
   VDAILYAMOUNT  MPAY_CLIENTSLIMITS.DAILYAMOUNT%TYPE;
   VWEEKLYCOUNT  MPAY_CLIENTSLIMITS.WEEKLYCOUNT%TYPE;
   VWEEKLYAMOUNT  MPAY_CLIENTSLIMITS.WEEKLYAMOUNT%TYPE;
   VMONTHLYCOUNT  MPAY_CLIENTSLIMITS.MONTHLYCOUNT%TYPE;
   VMONTHLYAMOUNT  MPAY_CLIENTSLIMITS.MONTHLYAMOUNT%TYPE;
   VYEARLYCOUNT  MPAY_CLIENTSLIMITS.YEARLYCOUNT%TYPE;
   VYEARLYAMOUNT  MPAY_CLIENTSLIMITS.YEARLYAMOUNT%TYPE;
   SWV_ROW_COUNT  int;
BEGIN
   SELECT COUNT(*) INTO STRICT VCOUNT FROM MPAY_CLIENTSLIMITS WHERE ACCOUNTID = PACCOUNTID AND MESSAGETYPEID = PMESSAGETYPEID;
   IF VCOUNT = 0 THEN
      SELECT NEXTVAL('MPAY_CLIENTSLIMITS_SEQ') INTO STRICT RID;
      INSERT INTO MPAY_CLIENTSLIMITS(ID,
       ACCOUNTID,
       MESSAGETYPEID,
       YEAR,
       DAILYDATE,
       DAILYCOUNT,
       DAILYAMOUNT,
       WEEKNUMBER,
       WEEKLYCOUNT,
       WEEKLYAMOUNT,
       MONTH,
       MONTHLYCOUNT,
       MONTHLYAMOUNT,
       YEARLYCOUNT,
       YEARLYAMOUNT)
    VALUES(RID,
     PACCOUNTID,
     PMESSAGETYPEID,
     EXTRACT(YEAR FROM PDATE),
     PDATE,
     PCOUNT,
     PAMOUNT,
     CAST(TO_CHAR(PDATE, 'WW') AS DOUBLE PRECISION),
     PCOUNT,
     PAMOUNT,
     EXTRACT(MONTH FROM PDATE),
     PCOUNT,
     PAMOUNT,
     PCOUNT,
     PAMOUNT);
ELSE
      SELECT YEAR, DAILYDATE, WEEKNUMBER, MONTH, DAILYCOUNT, DAILYAMOUNT, WEEKLYCOUNT, WEEKLYAMOUNT, MONTHLYCOUNT, MONTHLYAMOUNT, YEARLYCOUNT, YEARLYAMOUNT
      INTO STRICT VYEAR,VDAILYDATE,VWEEKNUMBER,VMONTH,VDAILYCOUNT,VDAILYAMOUNT,VWEEKLYCOUNT,
      VWEEKLYAMOUNT,VMONTHLYCOUNT,VMONTHLYAMOUNT,VYEARLYCOUNT,VYEARLYAMOUNT FROM MPAY_CLIENTSLIMITS WHERE ACCOUNTID = PACCOUNTID AND MESSAGETYPEID = PMESSAGETYPEID;
      IF VYEAR <> EXTRACT(YEAR FROM PDATE) THEN
         VYEAR := EXTRACT(YEAR FROM PDATE);
         VDAILYDATE := PDATE;
         VWEEKNUMBER := CAST(TO_CHAR(PDATE,'WW') AS DOUBLE PRECISION);
         VMONTH := EXTRACT(MONTH FROM PDATE);
         VDAILYCOUNT := PCOUNT;
         VWEEKLYCOUNT := PCOUNT;
         VMONTHLYCOUNT := PCOUNT;
         VYEARLYCOUNT := PCOUNT;
         VDAILYAMOUNT := PAMOUNT;
         VWEEKLYAMOUNT := PAMOUNT;
         VMONTHLYAMOUNT := PAMOUNT;
         VYEARLYAMOUNT := PAMOUNT;
   ELSE
         VYEARLYCOUNT := VYEARLYCOUNT + PCOUNT;
         VYEARLYAMOUNT := VYEARLYAMOUNT + PAMOUNT;
         IF VDAILYDATE <> PDATE THEN
            VDAILYDATE := PDATE;
            VDAILYCOUNT := PCOUNT;
            VDAILYAMOUNT := PAMOUNT;
      ELSE
            VDAILYCOUNT := VDAILYCOUNT + PCOUNT;
            VDAILYAMOUNT := VDAILYAMOUNT + PAMOUNT;
         END IF;
         IF VWEEKNUMBER <> CAST(TO_CHAR(PDATE,'WW') AS DOUBLE PRECISION) THEN
            VWEEKNUMBER := CAST(TO_CHAR(PDATE,'WW') AS DOUBLE PRECISION);
            VWEEKLYCOUNT := PCOUNT;
            VWEEKLYAMOUNT := PAMOUNT;
      ELSE
            VWEEKLYCOUNT := VWEEKLYCOUNT + PCOUNT;
            VWEEKLYAMOUNT := VWEEKLYAMOUNT + PAMOUNT;
         END IF;
         IF VMONTH <> EXTRACT(MONTH FROM PDATE) THEN
            VMONTH := EXTRACT(MONTH FROM PDATE);
            VMONTHLYCOUNT := PCOUNT;
            VMONTHLYAMOUNT := PAMOUNT;
      ELSE
            VMONTHLYCOUNT := VMONTHLYCOUNT + PCOUNT;
            VMONTHLYAMOUNT := VMONTHLYAMOUNT + PAMOUNT;
         END IF;
      END IF;
      IF VDAILYCOUNT < 0 THEN
         VDAILYCOUNT := 0;
      END IF;
      IF VDAILYAMOUNT < 0 THEN
         VDAILYAMOUNT := 0;
      END IF;
      IF VWEEKLYCOUNT < 0 THEN
         VWEEKLYCOUNT := 0;
      END IF;
      IF VWEEKLYAMOUNT < 0 THEN
         VWEEKLYAMOUNT := 0;
      END IF;
      IF VMONTHLYCOUNT < 0 THEN
         VMONTHLYCOUNT := 0;
      END IF;
      IF VMONTHLYAMOUNT < 0 THEN
         VMONTHLYAMOUNT := 0;
      END IF;
      IF VYEARLYCOUNT < 0 THEN
         VYEARLYCOUNT := 0;
      END IF;
      IF VYEARLYAMOUNT < 0 THEN
         VYEARLYAMOUNT := 0;
      END IF;
      UPDATE MPAY_CLIENTSLIMITS
      SET
      YEAR = VYEAR,DAILYDATE = VDAILYDATE,DAILYCOUNT = VDAILYCOUNT,DAILYAMOUNT = VDAILYAMOUNT,
      WEEKNUMBER = VWEEKNUMBER,WEEKLYCOUNT = VWEEKLYCOUNT,
      WEEKLYAMOUNT = VWEEKLYAMOUNT,MONTH = VMONTH,MONTHLYCOUNT = VMONTHLYCOUNT,
      MONTHLYAMOUNT = VMONTHLYAMOUNT,YEARLYCOUNT = VYEARLYCOUNT,YEARLYAMOUNT = VYEARLYAMOUNT
      WHERE ACCOUNTID = PACCOUNTID AND  MESSAGETYPEID = PMESSAGETYPEID;
     GET DIAGNOSTICS SWV_ROW_COUNT = ROW_COUNT;
   END IF;
   RROWCOUNT := SWV_ROW_COUNT;
   RETURN;
END; $BODY$
  LANGUAGE 'plpgsql' COST 100.0 SECURITY INVOKER;

