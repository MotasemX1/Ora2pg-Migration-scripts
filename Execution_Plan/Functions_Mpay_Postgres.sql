-- 

CREATE OR REPLACE FUNCTION updatebalances(in accountid int8, in amount numeric, out vrowscount int4, out vbalancebefore numeric, out vbalanceafter numeric)
  RETURNS record AS 
$BODY$
   DECLARE
   VTYPE  int8;
   UPD_ROW_COUNT int;
BEGIN
   SELECT BalanceTypeID, Balance
   INTO STRICT VTYPE,VBALANCEBEFORE
   FROM MPAY_ACCOUNTS
   WHERE ID = ACCOUNTID for update;
   VBALANCEAFTER := VBALANCEBEFORE + AMOUNT;
   IF VTYPE = 1 THEN
      UPDATE MPAY_ACCOUNTS SET balance = VBALANCEAFTER
      WHERE id = ACCOUNTID AND VBALANCEAFTER >= 0;
      GET DIAGNOSTICS UPD_ROW_COUNT = ROW_COUNT;
      VROWSCOUNT := UPD_ROW_COUNT;
ELSE 
      IF VTYPE = 2 THEN
         UPDATE MPAY_ACCOUNTS SET balance = VBALANCEAFTER
         WHERE id = ACCOUNTID AND VBALANCEAFTER <= 0;
         GET DIAGNOSTICS UPD_ROW_COUNT = ROW_COUNT;
         VROWSCOUNT := UPD_ROW_COUNT;
   ELSE
         UPDATE MPAY_ACCOUNTS SET balance = VBALANCEAFTER
         WHERE id = ACCOUNTID;
         GET DIAGNOSTICS UPD_ROW_COUNT = ROW_COUNT;
         VROWSCOUNT := UPD_ROW_COUNT;
      END IF;
   END IF;
   RETURN;
END; $BODY$
  LANGUAGE 'plpgsql' COST 100.0 SECURITY INVOKER;

