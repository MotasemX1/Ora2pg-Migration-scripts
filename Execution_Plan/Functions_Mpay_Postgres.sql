-- 

CREATE OR REPLACE FUNCTION transaction_summary_report(in sessid varchar, out c_dbtransaction refcursor)
  RETURNS refcursor AS 
$BODY$
BEGIN
   OPEN C_DBTRANSACTION for
   SELECT DISTINCT(SELECT LOCALTIMESTAMP) AS CREATIONDATE  ,SESSIONID,SUM(TOTALAMOUNT) AS TOTALAMOUNT ,SUM(totalcharge) AS TOTALCHARGE,
    (select txdirection.id from MPAY_TRANSACTIONDIRECTIONS txDirection where txDirection.id = transaction.DIRECTIONID) AS DIRECTION,
    (select id from MPAY_MESSAGETYPES where id =(select messagetypeid from MPAY_ENDPOINTOPERATIONS where id = transaction.refoperationid)) AS MESSAGETYPE
   FROM
   MPAY_TRANSACTIONS transaction
   WHERE
   PROCESSINGSTATUSID = 1 AND SESSIONID = SESSID
   GROUP BY REFOPERATIONID,DIRECTIONID,SESSIONID;
   RETURN;
END; $BODY$
  LANGUAGE 'plpgsql' COST 100.0 SECURITY INVOKER;

